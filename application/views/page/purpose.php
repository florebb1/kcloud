<h2>제2장 클라우드컴퓨팅 발전 기반의 조성</h2>
<ul class="basic">
	<li><b>제5조(기본계획 및 시행계획의 수립)</b> 
		<ul class="none">
			<li>① 과학기술정보통신부장관은 클라우드컴퓨팅의 발전과 이용 촉진 및 이용자 보호와 관련된 중앙행정기관(이하 "관계 중앙행정기관"이라 한다)의 클라우드컴퓨팅 관련 계획과 시책 등을 종합하여 3년마다 기본계획(이하 "기본계획"이라 한다)을 수립하고 「정보통신 진흥 및 융합 활성화 등에 관한 특별법」 제7조에 따른 정보통신 전략위원회의 심의를 거쳐 확정하여야 한다. <br>&lt;개정 2017.7.26.&gt;</li>
			<li>② 기본계획에는 다음 각 호의 사항이 포함되어야 한다.
				<ul class="number">
					<li>클라우드컴퓨팅 발전과 이용 촉진 및 이용자 보호를 위한 시책의 기본 방향</li>
					<li>클라우드컴퓨팅 산업의 진흥 및 이용 촉진을 위한 기반 조성에 관한 사항</li>
					<li>클라우드컴퓨팅의 도입과 이용 활성화에 관한 사항</li>
					<li>클라우드컴퓨팅기술의 연구개발 촉진에 관한 사항</li>
					<li>클라우드컴퓨팅 관련 전문인력의 양성에 관한 사항</li>
					<li>클라우드컴퓨팅 관련 국제협력과 해외진출 촉진에 관한 사항</li>
					<li>클라우드컴퓨팅서비스 이용자 정보 보호에 관한 사항</li>
					<li>클라우드컴퓨팅 관련 법령·제도 개선에 관한 사항</li>
					<li>클라우드컴퓨팅 관련 기술 및 산업 간 융합 촉진에 관한 사항</li>
					<li>그 밖에 클라우드컴퓨팅기술 및 클라우드컴퓨팅서비스의 발전과 안전한 이용환경 조성을 위하여 필요한 사항</li>
				</ul>
			</li>
			<li>③ 관계 중앙행정기관의 장은 기본계획에 따라 매년 소관별 시행계획(이하 "시행계획"이라 한다)을 수립·시행하여야 한다.</li>
			<li>④ 관계 중앙행정기관의 장은 다음 연도의 시행계획 및 전년도의 시행계획에 따른 추진실적을 대통령령으로 정하는 바에 따라 매년 과학기술정보통신부장관에게 제출하고, 과학기술정보통신부장관은 매년 시행계획에 따른 추진실적을 평가하여야 한다.  &lt;개정 2017.7.26.&gt;</li>
			<li>⑤ 제1항부터 제4항까지에서 규정한 사항 외에 기본계획 및 시행계획의 수립·시행, 추진실적의 제출·평가에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제6조(관계 기관의 협조)</b>
		<ul class="none">
			<li>① 과학기술정보통신부장관 및 관계 중앙행정기관의 장은 기본계획 또는 시행계획의 수립·시행을 위하여 필요한 경우에는 국가기관, 지방자치단체 및 「전자정부법」 제2조제3호에 따른 공공기관(이하 "국가기관등"이라 한다)의 장에게 협조를 요청할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 제1항에 따른 요청을 받은 자는 정당한 사유가 없으면 이에 따라야 한다.</li>
		</ul>
	</li>
	<li><b>제7조(실태조사)</b>
		<ul class="none">
			<li>① 과학기술정보통신부장관은 클라우드컴퓨팅에 관한 정책의 효과적인 수립·시행에 필요한 산업 현황과 통계를 확보하기 위하여 실태조사를 할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 과학기술정보통신부장관은 제1항에 따른 실태조사를 위하여 필요한 경우에는 클라우드컴퓨팅서비스 제공자나 그 밖의 관련 기관 또는 단체에 자료의 제출이나 의견의 진술 등을 요청할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 과학기술정보통신부장관은 관계 중앙행정기관의 장이 요구하는 경우 실태조사 결과를 통보하여야 한다.  &lt;개정 2017.7.26.&gt;</li>
			<li>④ 제1항부터 제3항까지에 따른 실태조사에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제8조(연구개발)</b>
		<ul class="none">
			<li>① 관계 중앙행정기관의 장은 클라우드컴퓨팅기술 및 클라우드컴퓨팅서비스에 관한 연구개발사업을 추진할 수 있다.</li>
			<li>② 관계 중앙행정기관의 장은 기업·연구기관 등에 제1항에 따른 연구개발사업을 수행하게 하고 그 사업 수행에 드는 비용의 전부 또는 일부를 지원할 수 있다.</li>
		</ul>
	</li>
	<li><b>제9조(시범사업)</b>
		<ul class="none">
			<li>① 관계 중앙행정기관의 장은 클라우드컴퓨팅기술 및 클라우드컴퓨팅서비스의 이용·보급을 촉진하기 위하여 시범사업을 추진할 수 있으며, 시범사업의 추진과 관련하여 지방자치단체에 협력을 요청할 수 있다.</li>
			<li>② 관계 중앙행정기관의 장은 제1항에 따른 시범사업에 참여하는 자에게 재정적 지원을 할 수 있다.</li>
		</ul>
	</li>
	<li><b>제10조(세제 지원)</b>국가와 지방자치단체는 클라우드컴퓨팅기술 및 클라우드컴퓨팅서비스의 발전과 이용 촉진을 위하여 「조세특례제한법」, 「지방세특례제한법」, 그 밖의 조세 관련 법률에서 정하는 바에 따라 조세감면 등 필요한 조치를 할 수 있다.
	</li>
	<li><b>제11조(중소기업에 대한 지원)</b> 
		<ul class="none">
			<li>① 정부는 클라우드컴퓨팅의 발전과 이용 촉진 및 이용자 보호를 위하여 클라우드컴퓨팅 관련 중소기업(「중소기업기본법」 제2조에 따른 중소기업을 말한다. 이하 같다)에 다음 각 호의 지원을 할 수 있다.
				<ul class="number">
					<li>클라우드컴퓨팅서비스에 관한 정보 제공 및 자문</li>
					<li>이용자 정보를 보호하기 위하여 필요한 기술 및 경비의 지원</li>
					<li>클라우드컴퓨팅 관련 전문인력의 양성</li>
					<li>그 밖에 클라우드컴퓨팅 관련 중소기업의 육성을 위하여 필요한 사항</li>
				</ul>
			</li>
