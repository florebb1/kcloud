<h1 class="t_center">클라우드컴퓨팅 발전 및<span class="mobile_br"></span> 이용자 보호에 관한 법률</h1>
<h4 class="t_center">( 약칭: 클라우드컴퓨팅법 )<br>[시행 2017.7.26.] [법률 제14839호, 2017.7.26., 타법개정] </h4>

<h2>제1장 총칙</h2>
<ul class="basic">
	<li><b>제1조(목적)</b> 이 법은 클라우드컴퓨팅의 발전 및 이용을 촉진하고 클라우드컴퓨팅서비스를 안전하게 이용할 수 있는 환경을 조성함으로써 국민생활의 향상과 국민경제릐 발전에 이바지함을 목적으로 한다.
	</li>
	<li><b>제2조(정의)</b> 이 법에서 사용하는 용어의 뜻은 다음과 같다.
		<ul class="number">
			<li>"클라우드컴퓨팅"(Cloud Computing)이란 직접 공유된 정보통신기기, 정보통신설비, 소프트웨어 등 정보통신자원(이하 "정보통신자원"이라 한다)을 이용자의 요구나 수요 변화에 따라 정보통신망을 통하여 신축적으로 이용할 수 있도록 하는 정보처리체계를 말한다.</li>
			<li>"클라우드컴퓨팅기술"이란 클라우드컴퓨팅의 구축 및 이용에 관한 정보통신기술로서의 가상화 기술, 분산처리 기술 등 대통령령으로 정하는 것을 말한다.</li>
			<li>"클라우드컴퓨팅서비스"란 클라우드컴퓨팅을 활용하여 상용(商用)으로 타인에게 정보통신자원을 제공하는 서비스로서 대통령령으로 정하는 것을 말한다.</li>
			<li>"이용자 정보"란 클라우드컴퓨팅서비스 이용자(이하 "이용자"라 한다)가 클라우드컴퓨팅서비스를 이용하여 클라우드컴퓨팅서비스를 제공하는 자(이하 "클라우드컴퓨팅서비스 제공자"라 한다)의 정보통신자원에 저장하는 정보(「국자정보화 기본법」 제3조제1호에 따른 정보를 말한다)로서 이용자가 소유 또는 관리하는 정보를 말한다.</li>
		</ul>
	</li>
	<li><b>제3조(국가 등의 책무)</b> 
		<ul class="none">
			<li>① 국가와 지방자치단체는 클라우드컴퓨팅의 발전 및 이용 촉진, 클라우드컴퓨팅서비스의 안전한 이용 환경 조성 등에 필요한 시책을 마련하여야 한다.</li>
			<li>② 클라우드컴퓨팅서비스 제공자는 이용자 정보를 보호하고 신뢰할 수 있는 클라우드컴퓨팅서비스를 제공하도록 노력하여야 한다.</li>
			<li>③ 이용자는 클라우드컴퓨팅서비스의 안전성을 해치지 아니하도록 하여야 한다.</li>
		</ul>
	</li>
	<li><b>제4조(다른 법률과의 관계)</b> 이 법은 클라우드컴퓨팅의 발전과 이용 촉진 및 이용자 보호에 관하여 다른 법률에 우선하여 적용하여야 한다. 다만 개인정보 보호에 관하여는 「개인정보 보호법」 「정보통신망 이용촉진 및 정보보호 등에 관한 법률」 등 관련 법률에서 정하는 바에 따른다.
	</li>
</ul>
