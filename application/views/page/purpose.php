<h2>제3장 클라우드컴퓨팅서비스의 이용 촉진</h2>
<ul class="basic">
	<li><b>제20조(공공기관의 클라우드컴퓨팅서비스 이용 촉진)</b> 정부는 공공기관이 업무를 위하여 클라우드컴퓨팅서비스 제공자의 클라우드컴퓨팅서비스를 이용할 수 있도록 노력하여야 한다.
	</li>
	<li><b>제21조(전산시설등의 구비)</b> 다른 법령에서 인가·허가·등록·지정 등의 요건으로 전산 시설·장비·설비 등(이하 "전산시설등"이라 한다)을 규정한 경우 해당 전산시설등에 클라우드컴퓨팅서비스가 포함되는 것으로 본다. 다만, 다음 각 호의 어느 하나에 해당하는 경우에는 그러하지 아니하다.
		<ul class="number">
			<li>해당 법령에서 클라우드컴퓨팅서비스의 이용을 명시적으로 금지한 경우</li>
			<li>해당 법령에서 회선 또는 설비의 물리적 분리구축 등을 요구하여 사실상 클라우드컴퓨팅서비스 이용을 제한한 경우</li>
			<li>해당 법령에서 요구하는 전산시설등의 요건을 충족하지 못하는 클라우드컴퓨팅서비스를 이용하는 경우</li>
		</ul>
	</li>
	<li><b>제22조(상호 운용성의 확보)</b> 과학기술정보통신부장관은 클라우드컴퓨팅서비스의 상호 운용성을 확보하기 위하여 필요한 경우에는 클라우드컴퓨팅서비스 제공자에게 협력 체계를 구축하도록 권고할 수 있다.  &lt;개정 2017.7.26.&gt;
	</li>
</ul>

