	</li>
	<li><b> 제35조(벌칙)</b>   제32조를 위반하여 위탁받은 업무를 수행하는 과정에서 알게 된 비밀을 누설하는 자는 3년 이하의 징역 또는 3천만원 이하의 벌금에 처한다.
