				</ul>
			</li>
			<li>② 제1항에 따른 과태료는 대통령령으로 정하는 바에 따라 과학기술정보통신부장관이 부과·징수한다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
</ul>
