<h2>제4장 클라우드컴퓨팅서비스의 신뢰성 향상 및 이용자 보호</h2>
<ul class="basic">
	<li><b>제23조(신뢰성 향상)</b>
		<ul class="none">
			<li> ① 클라우드컴퓨팅서비스 제공자는 클라우드컴퓨팅서비스의 품질·성능 및 정보보호 수준을 향상시키기 위하여 노력하여야 한다.</li>
			<li>② 과학기술정보통신부장관은 클라우드컴퓨팅서비스의 품질·성능에 관한 기준 및 정보보호에 관한 기준(관리적·물리적·기술적 보호조치를 포함한다)을 정하여 고시하고, 클라우드컴퓨팅서비스 제공자에게 그 기준을 지킬 것을 권고할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 과학기술정보통신부장관이 제2항에 따라 클라우드컴퓨팅서비스의 품질·성능에 관한 기준을 고시하려는 경우에는 미리 방송통신위원회의 의견을 들어야 한다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
	<li><b>제24조(표준계약서)</b>
		<ul class="none">
			<li> ① 과학기술정보통신부장관은 이용자를 보호하고 공정한 거래질서를 확립하기 위하여 공정거래위원회와 협의를 거쳐 클라우드컴퓨팅서비스 관련 표준계약서를 제정·개정하고, 클라우드컴퓨팅서비스 제공자에게 그 사용을 권고할 수 있다. 이 경우 클라우드컴퓨팅서비스 제공자, 이용자 등의 의견을 들을 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 과학기술정보통신부장관이 제1항에 따라 표준계약서를 제정·개정하려는 경우에는 미리 방송통신위원회의 의견을 들어야 한다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
	<li><b>제25조(침해사고 등의 통지 등)</b> 
		<ul class="none">
			<li> ① 클라우드컴퓨팅서비스 제공자는 다음 각 호의 어느 하나에 해당하는 경우에는 지체 없이 그 사실을 해당 이용자에게 알려야 한다.
				<ul class="number">
					<li>「정보통신망 이용촉진 및 정보보호 등에 관한 법률」 제2조제7호에 따른 침해사고(이하 "침해사고"라 한다)가 발생한 때</li>
					<li>이용자 정보가 유출된 때</li>
					<li>사전예고 없이 대통령령으로 정하는 기간(당사자 간 계약으로 기간을 정하였을 경우에는 그 기간을 말한다) 이상 서비스 중단이 발생한 때</li>
				</ul>
			</li>
			<li>② 클라우드컴퓨팅서비스 제공자는 제1항제2호에 해당하는 경우에는 즉시 그 사실을 과학기술정보통신부장관에게 알려야 한다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 과학기술정보통신부장관은 제2항에 따른 통지를 받거나 해당 사실을 알게 되면 피해 확산 및 재발의 방지와 복구 등을 위하여 필요한 조치를 할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>④ 제1항부터 제3항까지의 규정에 따른 통지 및 조치에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제26조(이용자 보호 등을 위한 정보 공개)</b>
		<ul class="none">
			<li>① 이용자는 클라우드컴퓨팅서비스 제공자에게 이용자 정보가 저장되는 국가의 명칭을 알려 줄 것을 요구할 수 있다.</li>
			<li>② 정보통신서비스(「정보통신망 이용촉진 및 정보보호 등에 관한 법률」 제2조제2호에 따른 정보통신서비스를 말한다. 이하 제3항에서 같다)를 이용하는 자는 정보통신서비스 제공자(「정보통신망 이용촉진 및 정보보호 등에 관한 법률」 제2조제3호에 따른 정보통신서비스 제공자를 말한다. 이하 제3항에서 같다)에게 클라우드컴퓨팅서비스 이용 여부와 자신의 정보가 저장되는 국가의 명칭을 알려 줄 것을 요구할 수 있다.</li>
			<li>③ 과학기술정보통신부장관은 이용자 또는 정보통신서비스 이용자의 보호를 위하여 필요하다고 인정하는 경우에는 클라우드컴퓨팅서비스 제공자 또는 정보통신서비스 제공자에게 제1항 및 제2항에 따른 정보를 공개하도록 권고할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>④ 과학기술정보통신부장관이 제3항에 따라 정보를 공개하도록 권고하려는 경우에는 미리 방송통신위원회의 의견을 들어야 한다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
	<li><b>제27조(이용자 정보의 보호)</b> 
		<ul class="none">
			<li>① 클라우드컴퓨팅서비스 제공자는 법원의 제출명령이나 법관이 발부한 영장에 의하지 아니하고는 이용자의 동의 없이 이용자 정보를 제3자에게 제공하거나 서비스 제공 목적 외의 용도로 이용할 수 없다. 클라우드컴퓨팅서비스 제공자로부터 이용자 정보를 제공받은 제3자도 또한 같다.</li>
			<li> ② 클라우드컴퓨팅서비스 제공자는 이용자 정보를 제3자에게 제공하거나 서비스 제공 목적 외의 용도로 이용할 경우에는 다음 각 호의 사항을 이용자에게 알리고 동의를 받아야 한다. 다음 각 호의 어느 하나의 사항이 변경되는 경우에도 또한 같다.
				<ul class="number">
					<li>이용자 정보를 제공받는 자</li>
					<li>이용자 정보의 이용 목적(제공 시에는 제공받는 자의 이용 목적을 말한다)</li>
					<li>이용 또는 제공하는 이용자 정보의 항목</li>
					<li>이용자 정보의 보유 및 이용 기간(제공 시에는 제공받는 자의 보유 및 이용 기간을 말한다)</li>
					<li>동의를 거부할 권리가 있다는 사실 및 동의 거부에 따른 불이익이 있는 경우에는 그 불이익의 내용</li>
				</ul>
			</li>
			<li>③ 클라우드컴퓨팅서비스 제공자는 이용자와의 계약이 종료되었을 때에는 이용자에게 이용자 정보를 반환하여야 하고 클라우드컴퓨팅서비스 제공자가 보유하고 있는 이용자 정보를 파기하여야 한다. 다만, 이용자가 반환받지 아니하거나 반환을 원하지 아니하는 등의 이유로 사실상 반환이 불가능한 경우에는 이용자 정보를 파기하여야 한다.</li>
			<li>④ 클라우드컴퓨팅서비스 제공자는 사업을 종료하려는 경우에는 그 이용자에게 사업 종료 사실을 알리고 사업 종료일 전까지 이용자 정보를 반환하여야 하며 클라우드컴퓨팅서비스 제공자가 보유하고 있는 이용자 정보를 파기하여야 한다. 다만, 이용자가 사업 종료일 전까지 반환받지 아니하거나 반환을 원하지 아니하는 등의 이유로 사실상 반환이 불가능한 경우에는 이용자 정보를 파기하여야 한다.</li>
			<li>⑤ 제3항 및 제4항에도 불구하고 클라우드컴퓨팅서비스 제공자와 이용자 간의 계약으로 특별히 다르게 정한 경우에는 그에 따른다.</li>
			<li>⑥ 제3항 및 제4항에 따른 이용자 정보의 반환 및 파기의 방법·시기, 계약 종료 및 사업 종료 사실의 통지 방법 등에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제28조(이용자 정보의 임치)</b>
		<ul class="none">
			<li>① 클라우드컴퓨팅서비스 제공자와 이용자는 전문인력과 설비 등을 갖춘 기관[이하 "수치인"(受置人)이라 한다]과 서로 합의하여 이용자 정보를 수치인에게 임치(任置)할 수 있다.</li>
			<li>② 이용자는 제1항에 따른 합의에서 정한 사유가 발생한 때에 수치인에게 이용자 정보의 제공을 요구할 수 있다.</li>
		</ul>
	</li>
	<li><b>제29조(손해배상책임)</b> 이용자는 클라우드컴퓨팅서비스 제공자가 이 법의 규정을 위반한 행위로 인하여 손해를 입었을 때에는 그 클라우드컴퓨팅서비스 제공자에게 손해배상을 청구할 수 있다. 이 경우 해당 클라우드컴퓨팅서비스 제공자는 고의 또는 과실이 없음을 입증하지 아니하면 책임을 면할 수 없다.
	</li>
</ul>

<hr>
<h2>제5장 보칙</h2>
<ul class="basic">
	<li><b>제30조(사실조사 및 시정조치)</b>
		<ul class="none">
			<li> ① 과학기술정보통신부장관은 클라우드컴퓨팅서비스 제공자가 이 법을 위반한 행위가 있다고 인정하면 소속 공무원에게 이를 확인하기 위하여 필요한 조사를 하게 할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 과학기술정보통신부장관은 제1항에 따른 조사를 위하여 필요하면 소속 공무원에게 클라우드컴퓨팅서비스 제공자의 사무소·사업장에 출입하여 장부·서류, 그 밖의 자료나 물건을 조사하게 할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 과학기술정보통신부장관은 제1항에 따라 조사를 하는 경우 조사 7일 전까지 조사 기간·이유·내용 등을 포함한 조사계획을 해당 클라우드컴퓨팅서비스 제공자에게 알려야 한다. 다만, 긴급한 경우나 사전에 통지하면 증거인멸 등으로 조사 목적을 달성할 수 없다고 인정하는 경우에는 그러하지 아니하다.  &lt;개정 2017.7.26.&gt;</li>
			<li>④ 제2항에 따라 클라우드컴퓨팅서비스 제공자의 사무소·사업장에 출입하여 조사하는 사람은 그 권한을 표시하는 증표를 관계인에게 보여주어야 하며, 조사를 할 때에는 해당 사무소나 사업장의 관계인을 참여시켜야 한다.</li>
			<li>⑤ 과학기술정보통신부장관은 제25조제1항 또는 제27조를 위반한 클라우드컴퓨팅서비스 제공자에게 해당 위반행위의 중지나 시정을 위하여 필요한 조치를 명할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
	<li><b>제31조(위임 및 위탁)</b>
		<ul class="none">
			<li>① 이 법에 따른 과학기술정보통신부장관 및 관계 중앙행정기관의 장의 권한은 대통령령으로 정하는 바에 따라 그 일부를 그 소속 기관의 장에게 위임할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 이 법에 따른 과학기술정보통신부장관 및 관계 중앙행정기관의 업무는 대통령령으로 정하는 바에 따라 그 일부를 전문기관에 위탁할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
	<li><b>제32조(비밀 엄수)</b> 이 법에 따라 위탁받은 업무에 종사하거나 종사하였던 자는 업무를 수행하는 과정에서 알게 된 클라우드컴퓨팅서비스 제공자의 사업상 비밀을 누설하여서는 아니 된다.
	</li>
	<li><b>제33조(벌칙 적용 시 공무원 의제)</b>  제31조제2항에 따라 위탁받은 업무에 종사하는 전문기관의 임직원은 「형법」 제129조부터 제132조까지의 규정에 따른 벌칙을 적용할 때에는 공무원으로 본다.
	</li>
</ul>

