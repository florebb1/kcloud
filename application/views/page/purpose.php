	</li>
	<li><b>제37조(과태료)</b> 
		<ul class="none">
			<li> ① 다음 각 호의 어느 하나에 해당하는 자에게는 1천만원 이하의 과태료를 부과한다.  &lt;개정 2017.7.26.&gt;
				<ul class="number">
					<li>제25조제1항을 위반하여 침해사고, 이용자 정보 유출, 서비스 중단 발생 사실을 이용자에게 알리지 아니한 자</li>
					<li>제25조제2항을 위반하여 이용자 정보 유출 발생 사실을 과학기술정보통신부장관에게 알리지 아니한 자</li>
					<li>제27조제3항 또는 제4항을 위반하여 이용자 정보를 반환하지 아니하거나 파기하지 아니한 자</li>
