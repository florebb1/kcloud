		<ul class="none">
			<li>① 국가기관등은 클라우드컴퓨팅을 도입하도록 노력하여야 한다.</li>
			<li>② 정부는 「국가정보화 기본법」에 따른 국가정보화 정책이나 사업 추진에 필요한 예산을 편성할 때에는 클라우드컴퓨팅 도입을 우선적으로 고려하여야 한다.</li>
		</ul>
	</li>
	<li><b>제13조(클라우드컴퓨팅 사업의 수요예보)</b>
		<ul class="none">
			<li>① 국가기관등의 장은 연 1회 이상 소관 기관의 클라우드컴퓨팅 사업의 수요정보를 과학기술정보통신부장관에게 제출하여야 한다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 과학기술정보통신부장관은 제1항에 따라 접수된 클라우드컴퓨팅 수요정보를 연 1회 이상 클라우드컴퓨팅서비스 제공자에게 공개하여야 한다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 제1항에 따른 제출 및 제2항에 따른 공개의 구체적인 횟수·시기·방법·절차 등에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제14조(전문인력의 양성)</b> 
		<ul class="none">
			<li> ① 과학기술정보통신부장관은 클라우드컴퓨팅에 관한 전문인력을 양성하기 위하여 필요한 정책을 수립하고 추진할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 과학기술정보통신부장관은 클라우드컴퓨팅 관련 교육훈련을 실시하는 교육기관 중 대통령령으로 정하는 요건을 갖춘 기관을 지정하여 필요한 경비의 전부 또는 일부를 지원할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 과학기술정보통신부장관은 제2항에 따라 지정한 교육기관이 다음 각 호의 어느 하나에 해당하는 경우 그 지정을 취소할 수 있다. 다만 제1호에 해당하는 경우에는 그 지정을 취소하여야 한다.   &lt;개정 2017.7.26.&gt;
				<ul class="number">
					<li>거짓이나 그 밖의 부정한 방법으로 지정받은 경우</li>
					<li>제2항에 따른 지정 요건에 적합하지 아니하게 된 경우</li>
					<li>교육기관 지정일부터 1년 이상 교육 실적이 없는 경우</li>
				</ul>
			</li>
			<li>④ 제1항부터 제3항까지에 따른 정책의 수립, 교육기관의 지정 요건, 지정 및 지정 취소 절차와 지원 내용 등에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제15조(국제협력과 해외진출의 촉진)</b> 정부는 클라우드컴퓨팅 관련 국제협력과 클라우드컴퓨팅기술 및 클라우드컴퓨팅서비스의 해외진출을 촉진하기 위하여 다음 각 호의 사업을 추진할 수 있다.
		<ul class="number">
			<li>클라우드컴퓨팅 관련 정보·기술·인력의 국제교류</li>
			<li>클라우드컴퓨팅 관련 전시회 등 홍보와 해외 마케팅</li>
			<li>국가 간 클라우드컴퓨팅 공동 연구·개발</li>
			<li>클라우드컴퓨팅 관련 해외진출에 관한 정보의 수집·분석 및 제공</li>
			<li>클라우드컴퓨팅 관련 국제협력의 실효성 확보를 위한 국가 간 공조</li>
			<li>그 밖에 클라우드컴퓨팅 관련 국제협력 및 해외진출 촉진을 위하여 필요한 사업</li>
		</ul>
	</li>
	<li><b>제16조(클라우드컴퓨팅기술 기반 집적정보통신시설의 구축 지원)</b>
		<ul class="none">
			<li>① 국가와 지방자치단체는 클라우드컴퓨팅의 발전과 이용을 촉진하기 위하여 클라우드컴퓨팅기술을 이용하여 집적된 정보통신시설을 구축하려는 자에게 행정적·재정적·기술적 지원을 할 수 있다.</li>
			<li>② 제1항에 따른 지원의 대상, 방법 및 절차 등에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
	<li><b>제17조(산업단지의 조성)</b>
		<ul class="none">
			<li>① 국가와 지방자치단체는 클라우드컴퓨팅 산업 관련 기술의 연구·개발과 전문인력 양성 등을 통하여 클라우드컴퓨팅 산업의 진흥과 클라우드컴퓨팅의 활용 촉진을 위한 산업단지를 조성할 수 있다.</li>
			<li>② 산업단지의 조성은 「산업입지 및 개발에 관한 법률」에 따른 국가산업단지, 일반산업단지 또는 도시첨단산업단지의 지정·개발 절차에 따른다.</li>
			<li>③ 과학기술정보통신부장관은 산업단지의 조성을 촉진하기 위하여 필요하다고 인정하는 경우에는 국토교통부장관에게 산업단지로의 지정을 요청할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
		</ul>
	</li>
	<li><b>제18조(공정한 경쟁 환경 조성 등)</b>
		<ul class="none">
			<li>① 정부는 대기업(「중소기업기본법」 제2조에 따른 중소기업 및 「중견기업 성장촉진 및 경쟁력 강화에 관한 특별법」 제2조제1호에 따른 중견기업이 아닌 기업을 말한다)인 클라우드컴퓨팅서비스 제공자와 중소기업인 클라우드컴퓨팅서비스 제공자 간의 공정한 경쟁환경을 조성하고 상호간 협력을 촉진하여야 한다.</li>
			<li>② 대기업인 클라우드컴퓨팅서비스 제공자는 중소기업인 클라우드컴퓨팅서비스 제공자에게 합리적인 이유 없이 그 지위를 이용하여 불공정한 계약을 강요하거나 부당한 이익을 취득하여서는 아니 된다.</li>
			<li>③ 정부는 클라우드컴퓨팅 산업의 공정한 경쟁 환경 조성을 위하여 클라우드컴퓨팅 산업 경쟁 환경의 현황 분석 및 평가, 그 밖에 공정한 유통 환경을 조성하기 위하여 필요한 사업을 할 수 있다.</li>
		</ul>
	</li>
	<li><b>제19조(전담기관의 지정 등)</b>
		<ul class="none">
			<li>① 과학기술정보통신부장관은 클라우드컴퓨팅산업 진흥과 클라우드컴퓨팅 이용 촉진을 위하여 필요한 때에는 전담기관을 지정할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>② 과학기술정보통신부장관은 전담기관의 사업 수행에 필요한 경비의 전부 또는 일부를 지원할 수 있다.  &lt;개정 2017.7.26.&gt;</li>
			<li>③ 전담기관의 지정 및 운영 등에 필요한 사항은 대통령령으로 정한다.</li>
		</ul>
	</li>
</ul>

