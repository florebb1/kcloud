<h2 class="t_center">클라우드컴퓨팅서비스 품질ㆍ성능에 관한 기준</h2>
<hr>
<ul class="basic">
	<li><b>제1조(목적)</b> 이기준은 ｢클라우드컴퓨팅발전및이용자보호에관한법률｣제23조제2항에따라이용자보호및클라우드컴퓨팅서비스활성화를위하여클라우드컴퓨팅서비스의품질ㆍ성능에관한기준(이하"품질ㆍ성능기준"이라한다)에필요한사항을정함을목적으로한다.
	</li>
	<li><b>제2조(용어의 정의)</b> 이 고시에서 사용하는 용어의 정의는 다음과 같다.
		<ul class="number">
			<li>"가용성"이란 클라우드컴퓨팅서비스가 장애 없이 정상적으로 운영되는 능력을 말한다.</li>
			<li>"응답성"이란 클라우드컴퓨팅서비스 이용자의 요청에 따라 얼마나 빨리 응답하는 가에 대한 능력을 말한다.</li>
			<li>"확장성"이란 클라우드컴퓨팅서비스 이용자의 요청에 따라 자원의 양을 할당할 수 능력을 말한다.</li>
			<li>"신뢰성"이란 클라우드컴퓨팅서비스 제공자가 클라우드컴퓨팅서비스를 정상적으로 운영할 수 있는 능력을 말한다. </li>
			<li>"서비스 지속성"이란 클라우드컴퓨팅서비스 제공자가 클라우드컴퓨팅서비스를 안정적으로 계속하여 제공할 수 있는 능력을 말한다.</li>
			<li>"서비스지원"이란클라우드컴퓨팅서비스의이용편의성을제공하기위한능력을말한다. </li>
			<li>"고객대응"이란클라우드컴퓨팅서비스를이용하는고객요청에대응하는일련의활동을말한다. </li>
		</ul>
	</li>
	<li><b>제3조(품질ㆍ성능적용대상) </b> 품질ㆍ성능은다음각호의어느하나에해당하는서비스를말한다. 
		<ul class="number">
			<li>서버, 저장장치, 네트워크등을제공하는서비스</li>
			<li>제1호의서비스를기반으로응용프로그램등소프트웨어의개발ㆍ배포ㆍ운영ㆍ관리등을위한환경을제공하는서비스</li>
			<li>제1호 및 제2호의 서비스를 기반으로 응용프로그램등 소프트웨어를 제공하는 서비스</li>
		</ul>
	</li>
	<li><b>제4조(품질ㆍ성능기준) </b> 
		<ul class="none">
			<li>①품질ㆍ성능기준은 다음 각 호를 명시하여야 한다.
				<ul class="number">
					<li>(가용성) 클라우드컴퓨팅서비스 제공자의 가용률 측정을 위한 기능보유 및 가용률 유지능력</li>
					<li>(응답성) 클라우드컴퓨팅서비스 제공자의 응답시간 측정을 위한 기능보유 및 응답시간 유지능력</li>
					<li>(확장성) 클라우드컴퓨팅서비스 제공자의 이용자 요구에 따라 자원의 양을 줄이거나 늘릴 수 있는 기능 및 시스템보유</li>
					<li>(신뢰성) 클라우드컴퓨팅서비스 제공자의 서비스회복시간, 백업주기, 백업준수율 및 백업데이터 보관기간측정을 위한 기능 및 시스템보유</li>
					<li>(서비스지속성) 클라우드컴퓨팅서비스제공자의재무상태및기술보증,서비스추진전략, 조직및인력등을포함한서비스제공능력</li>
					<li>(서비스지원) 클라우드컴퓨팅서비스제공자의단말ㆍ운영체계등이용자지원기능과보상대책및기술지원문서, 모니터링웹사이트등을포함한서비스지원체계</li>
					<li>(고객대응) 클라우드컴퓨팅서비스제공자의고객요청에대응하기위한고객대응체계와고객불만수집체계및처리절차</li>
				</ul>
			</li>
			<li>②품질ㆍ성능의세부기준은별표와같다. </li>
		</ul>
	</li>
	<li><b>제5조(품질ㆍ성능측정방법및절차)</b> 클라우드컴퓨팅서비스제공자는 제4조에대한세부적인측정방법및절차를제시하고, 이에따른측정결과를제공할수있다. 
	</li>
	<li><b>제6조(품질ㆍ성능기준적용) </b> 클라우드컴퓨팅서비스제공자는이기준을고려하여서비스를제공하고이용자를보호할수있도록표준계약서또는서비스수준협약에적용할수있다. 
	</li>
	<li><b>제7조(품질ㆍ성능기준제공확인) </b> 과학기술정보통신부장관은 클라우드컴퓨팅서비스제공자가 그서비스에대하여 이기준을충족하는지의확인을요청하는경우에는｢정보통신산업진흥법｣제26조에따른“정보통신산업진흥원”의장또는별도로지정하는기관에게확인하게할수있다. 
	</li>
	<li><b>제8조(재검토)</b> 과학기술정보통신부장관은 ｢훈령•예규등의발령및관리에관한규정(대통령훈령제248호)｣에따라이고시에대하여‘고시발령일’을기준으로매3년마다법령이나현실여건의변화등을검토하여이고시의폐지, 개정등의조치를하여야한다. 
