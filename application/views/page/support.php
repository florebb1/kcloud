<h3>① 이용자 서비스 정책수립</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 이용자를 지원하기 위해 필요한 사항들을 포함한 정책을 정의한다. 클라우드서비스 이용자를 지원하기 위한 정책에는 고객 정의, 고객 지원을 위한 기술/행정 문서 및 절차, 서비스 수준 정량화/보장(SLA), 고객과의 회의/평가/피드백 계획, 대 고객 서비스 교육 프로그램, 최신 서비스 제공계획 등이 포함될 수 있으며, 주기적으로 정책의 적정성 검토를 수행한다. </li>
</ul>
<hr>
<h3>② 서비스 제공방식 다양성</h3>
<ul class="basic">
	<li>클라우드서비스는 다양한 운영체제를 통해서 제공되며, 윈도우·리눅스 등 일반운영체제 및 안드로이드·iOS 등의 모바일 운영체제에도 원활하게 서비스 할 수 있다. PC·모바일 단말 등 다양한 단말을 통해서도 제공될 수 있다. </li>
</ul>
<hr>
<h3>③ 서비스 이전 정책 및 프로세스</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 서비스가 계약 요건에 부합하지 못하거나 운영을 중단해야 하는 경우 이용자가 다른 클라우드 제공자로 이전하여 서비스를 이용할 수 있도록 지원한다. 이를 위해 IaaS, PaaS, SaaS, 등 서비스의 형태에 따라 클라우드 시스템을 구성하는 하드웨어 및 소프트웨어를 다른 서비스 제공자로 이전하기 위한 절차를 마련한다.</li>
</ul>
<hr>
<h3>④ 이용자 교육</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 이용자가 서비스를 원활하게 이용할 수 있도록 서비스 교육 프로그램·매뉴얼을 마련한다. 또한, 클라우드서비스 이용자가 요청하는 경우 서비스 교육 프로그램·매뉴얼을 제공할 수 있다. </li>
</ul>
