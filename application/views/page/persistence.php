<h3>① 서비스 제공자의 재무상태</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 안정적인 클라우드서비스를 제공하기 위해 건전한 재무상태를 유지하거나, 경영상의 안정성을 투명하게 보여 줄 수 있는 인증(기술보증·이노비즈·벤처기업확인 등)을 보유할 수 있다. </li>
	<li>클라우드서비스 제공자는 서비스를 제공하기 위해 직접 또는 임차 계약을 통해 하드웨어 및 네트워크 장비, 소프트웨어·솔루션 등 서비스 제공 계획 대비 필요한 최소한의 전산 자원을 확보하여 운영한다. </li>
</ul>
<hr>
<h3>② 비전 및 중장기 목표 수립</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 안정적인 클라우드서비스를 제공하기 위해 기업 비전 및 하위 정책/전략간 연계성 등을 고려하여 기업의 중장기 목표를 마련한다. </li>
</ul>
<hr>
<h3>③ 서비스 지원 조직 및 인력</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 서비스를 안정적으로 제공하기 위해 구성 담당 인력·조직의 책임과 역할을 전체 조직체계·업무정의 등을 통하여 규정한다. 또한, 서비스를 안정적으로 제공하기 위해 필요한 기술·조직을 보유한다</li>
</ul>
<hr>
<h3>④ 서비스 지원 교육 프로그램</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 서비스를 안정적으로 제공하기 위해 클라우드서비스의 유지·개선에 필요한 교육 프로그램을 운영·실시할 수 있다. </li>
</ul>
<hr>
<h3>⑤ 예산계획 수립</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 서비스를 안정적으로 제공하기 위해 필요한 예산계획을 수립하고, 수립된 예산 계획에 따라 시행한다. </li>
</ul>
<hr>
<h3>⑥ 재무통제 및 승인 정책, 프로세스</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 서비스를 안정적으로 제공하기 위해 효과적인 재무통제 및 승인을 위한 정책·프로세스를 수립한다. 효과적인 재무통제 및 승인을 위한 정책·프로세스의 수립을 위해 예산·재무 보고 체계를 정립할 수 있다.</li>
</ul>
<hr>
<h3>⑦ 변경관리 프로세스 수립</h3>
<ul class="basic">
	<li>클라우드서비스 제공자는 서비스 및 시스템의 변경에 대해 그 내용을 명확히 정의하고, 변경 관리를 위한 프로세스를 수립한다. 변경관리 프로세스에는 변경 요청 기록·분류, 승인·점검 절차, 기관 내 전달 절차 등의 내용을 포함하여 정의한다. </li>
</ul>
<hr>
<h3>⑧ 서비스 개선 계획 및 정책</h3>
<ul class="basic">
	<li>서비스 변경 관리 결과에 따라 분석된 내용은 서비스 개선을 위해 관련 계획·정책 등에 반영할 수 있다</li>
